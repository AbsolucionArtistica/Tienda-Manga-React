// Categorías disponibles
export const categorias = ['Todos', 'Shonen', 'Seinen', 'Shoujo']

// Función para obtener todos los mangas (simula una llamada a API)
export const obtenerMangas = async () => {
  // Simular delay de red
  await new Promise(resolve => setTimeout(resolve, 1000))
  return mangasData
}

// Función para obtener mangas por categoría
export const obtenerMangasPorCategoria = async (categoria) => {
  await new Promise(resolve => setTimeout(resolve, 500))
  if (categoria === 'Todos') {
    return mangasData
  }
  return mangasData.filter(manga => manga.categoria === categoria)
}

// Función para buscar mangas
export const buscarMangas = async (termino) => {
  await new Promise(resolve => setTimeout(resolve, 300))
  return mangasData.filter(manga =>
    manga.nombre.toLowerCase().includes(termino.toLowerCase()) ||
    manga.autor.toLowerCase().includes(termino.toLowerCase())
  )
}

// Función para obtener un manga específico por ID
export const obtenerMangaPorId = (id) => {
  return mangasData.find(manga => manga.id === id)
}

// Función para formatear precios en pesos chilenos
export const formatearPrecio = (precio) => {
  return new Intl.NumberFormat('es-CL', {
    style: 'currency',
    currency: 'CLP',
    minimumFractionDigits: 0,
    maximumFractionDigits: 0
  }).format(precio)
}